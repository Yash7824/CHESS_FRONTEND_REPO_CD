import mongoose from 'mongoose';
const { Schema } = mongoose;

const UserFullSchema = new Schema({
  name: {
    type: String,
    required: true
  },
  email: {
    type: String,
    required: true,
    unique: true
  },
  password_encrypted: {
    type: String,
    required: true
  },
  password: {
    type: String,
    required: true
  },
  date: {
    type: Date,
    default: Date.now
  }
});

const UserFull = mongoose.model('userFull', UserFullSchema);
export default UserFull;